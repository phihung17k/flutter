    private DefaultListModel<Integer> getListModel(Set<Integer> rooms) {
        DefaultListModel<Integer> results = new DefaultListModel<>();
        for(Integer roomId : rooms) {
            results.addElement(roomId);
        }
        return results;
    }
    
    private void onLoadRoomsToJList() {
        rooms = roomDAO.getAllRooms2();
        listModel = getListModel(rooms.keySet());
        listRoom.setModel(listModel);
    }
    
