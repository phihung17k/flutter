    private void listRoomValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listRoomValueChanged
        // TODO add your handling code here:
        Integer roomId = listRoom.getSelectedValue();
        details = detailDAO.getDetails(roomId);
        Room tmp = roomDAO.getRoom(roomId);
        if (!details.isEmpty()) {
            chkBoxBooked.setSelected(true);
        }
        txtRoomName.setText(tmp.getName());
        txtPrice.setText(tmp.getPrice() + "");
    }//GEN-LAST:event_listRoomValueChanged

